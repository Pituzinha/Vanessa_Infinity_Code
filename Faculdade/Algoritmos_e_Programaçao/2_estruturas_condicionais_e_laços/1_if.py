#Verifique se o numero é positivo, negativo ou zero.

numero = int (input ("Digite um número:"))
if numero > 0:
    print ("O número é positivo.")
if numero < 0:
    print ("O número é negativo")
if numero == 0:
    print ("O número é zero")
    

